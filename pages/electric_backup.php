              <b>UPS (Uninterruptable Power Supply)</b><br>
              <p>
                Its a back-up power system used to ensure uninterrupted power for various electronic devices.<br>
                The three basic types of UPS Systems:
                <ul style="list-style: disc;">
                  <li>
                    <b>Standby Power System (SPS):</b>
                    <p>Most Commonly used to protect POS equipment and single work stations. Monitors the power line. When a problem is detected, the SPS switches to battery power.</p>
                  </li>
                  <li>
                    <b>Line Interactive:</b>
                    <p>Most commonly used UPS system for network environments. Used for small networks up to enterprise applications.  The inverter (a battery-to-AC power converter)  is always connected to the the UPS output. The inverter operates in reverse during times when the input AC power is normal to provide battery charging.</p>
                  </li>
                   <li>
                    <b>Double Conversion On-line UPS:</b>
                    <p>Most commonly used for mission critical applications: server farms, hospitals, etc. Avoids momentary power lapses by continuously providing power from its own inverter. The double conversion on-line UPS provides power from its own inverter even with proper power line functioning.</p>
                  </li>
